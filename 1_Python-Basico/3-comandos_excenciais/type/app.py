# TYPE é uma função Python que permite saber o tipo de dado de um elemento ou uma variável, porém precisamos usar ela em conjunto com o PRINT...
# SINTAXE:
type(1) # Dentro dos parênteses podemos colocar um dado, uma variável ou uma estrutura de dados:

