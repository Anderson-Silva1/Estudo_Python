# Indexação - Podemos usar os íncices ou Index's da nossa lista para pegar dados específicos:
# OBS: Na programação os indices começam do 0 (zero)

# INDEX           0           1          2
c_exLista1 = ["Anderson", "Rafaela", "Emanoel"]
print(c_exLista1[0]) # Anderson

# INDEX       0  1  2  3  4 
c_exLista2 = [1, 2, 3, 4, 5]
print(c_exLista2[3]) # 4

# INDEX        0    1    2
c_exLista3 = [1.0, 2.5, 3.7]
print(c_exLista3[2]) # 3.7

# INDEX        0      1
c_exLista4 = [True, False]
print(c_exLista4[0]) # True

# INDEX         0        1      2   3     4
c_exLista5 = [True, "Anderson", 1, 3.4, False]
print(c_exLista5[1]) # Anderson
