"""
    OPERADORES MATEMÁTICOS:
        1 - + (Soma)
        2 - - (Subtração)
        3 - * (Multiplicação)
        4 - / (Divisão)
        5 - ** (Exponênciação)
        6 - // (Quociente)
        7 - % (Módulo ou Resto)
        8 - abs (Absoluto)
"""

print(f"Exemplo de operador matematico '+' = {1 + 1}") # SOMA
print(f"Exemplo de operador matematico '-' = {10 - 1}") # SUBTRAÇÃO
print(f"Exemplo de operador matematico '*' = {1 * 5}") # MULTIPLICAÇÃO
print(f"Exemplo de operador matematico '/' = {10 / 2}") # DIVISÃO
print(f"Exemplo de operador matematico '//' = {10 // 3}") # QUOCIENTE
print(f"Exemplo de operador matematico '%' = {10 % 3}") # RESTO
print(f"Exemplo de operador matematico '**' = {10 ** 2}") # EXPONENTE
print(f"Exemplo de operador matematico 'abs' = {abs(-2.5)}\n")