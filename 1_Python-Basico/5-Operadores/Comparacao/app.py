""" Operadores de comparação servem para compararmos dois dados,
    e o resultado será um valor Boleano...

    No Python temos os seguintes operadores de comparação:
        < - Menor que
        > - Maior que
        <= - Menos ou Igual a que
        >= - Maior ou igual a que
        == - Igual
        != - Diferente
    """

print(f"Operador de comparacao '>' = {10 > 9}") # 10 é maior que 9? TRUE
print(f"Operador de comparacao '<' = {10 < 9}") # 10 é menos que 9? FALSE
print(f"Operador de comparacao '==' = {10 == 9}") # 10 é igual a 9? FALSE 
print(f"Operador de comparacao '>=' = {10 >= 9}") # 10 é maior ou igual a 9? TRUE
print(f"Operador de comparacao '<=' = {10 <= 9}") # 10 é menor ou igual a nova? FALSE
print(f"Operador de comparacao '!=' = {10 != 9}") # 10 é diferente de 9? TRUE