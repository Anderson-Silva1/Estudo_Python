# INDEXAÇÃO - A indexação será a mesma da LISTA:

# index           0  1  2
c_exTuplaIndex = (1, 2, 3)
print(f"Esse e um exemplo de indexacao das tuplas: {c_exTuplaIndex[1]}")

# INDEXAÇÃO NEGATIVA - A indexação negativa será a mesma da LISTA:

# index             -3 -2 -1
c_exTuplaIndexNeg = (1, 2, 3)
print(f"Esse e um exemplo de indexacao das tuplas: {c_exTuplaIndexNeg[-1]}")

# SLICING - A filosofia do Slicing será a mesma

# index             0  1  2
# index            -3 -2 -1
c_exTuplaSlicing = (1, 2, 3)
print(f"Esse e um exemplo de indexacao das tuplas: {c_exTuplaSlicing[ : : ]}")