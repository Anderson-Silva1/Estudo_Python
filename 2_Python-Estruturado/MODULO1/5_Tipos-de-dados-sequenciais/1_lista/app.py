# Listas - 
    # São usadas como vetores
    # São variáveis que armazenam mais de um dado em si
    # Possuem índices, o que significa que elas possuem listas ordenadas ou seja, possuem ordem
    # Dentro da lista é suortado todos os tipos de dados do PYTHON
    # Declaramos uma variável recebendo "[]", e dentro desses "[]" estará as informações da nossa lista separadas por ","

# Ex de lista:

c_exLista0 = []
print(c_exLista0) # []

c_exLista1 = ["Anderson", "Rafaela", "Emanoel"]
print(c_exLista1) # ['Anderson', 'Rafaela', 'Emanoel']

c_exLista2 = [1, 2, 3, 4, 5]
print(c_exLista2) # [1, 2, 3, 4, 5]

c_exLista3 = [1.0, 2.5, 3.7]
print(c_exLista3) # [1.0, 2.5, 3.7]

c_exLista4 = [True, False]
print(c_exLista4) # [True, False]

c_exLista5 = [True, "Anderson", 1, 3.4, False]
print(c_exLista5) # [True, 'Anderson', 1, 3.4, False]

print(type(c_exLista1)) # <class 'list'>
# O tipo desses dados será "LIST"